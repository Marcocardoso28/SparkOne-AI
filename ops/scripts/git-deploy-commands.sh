    echo -e "\n${YELLOW}âš ï¸  Tag ${VERSION} already exists${NC}"
    read -p "Delete and recreate? (y/N) " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        git tag -d "${VERSION}"
        echo -e "${GREEN}âœ… Deleted existing tag${NC}"
    else
        echo -e "${RED}âŒ Aborted${NC}"
        exit 1
