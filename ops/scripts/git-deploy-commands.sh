# Check if git is available
if ! command -v git &> /dev/null; then
    echo -e "${RED}âŒ Git is not installed${NC}"
    exit 1
fi

# Check if we're in a git repository
if [ ! -d .git ]; then
    echo -e "${RED}âŒ Not a git repository${NC}"
    exit 1
fi

# Get current branch
